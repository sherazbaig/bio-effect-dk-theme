/**
 * Plugin: Basis custom content tool plugin
 * -----------------------------------------------------------------------------
 * Custom content tool functionality.
 * - Updates section Liquid files with section schema attribute value.
 * - Duplicates section Liquid files as snippets and removes schema tag.
 *
 */
const fs = require('fs-extra')
const path = require('path')
const fileSync = require('@we-make-websites/file-sync')
const Tny = require('@we-make-websites/tannoy')
const { Compilation } = require('webpack')

const debug = require('../helpers/debug')
const Paths = require('../helpers/paths')

/**
 * Set variables.
 */
const pluginName = 'BasisCustomContentToolPlugin'

/**
 * Tap into hooks and add functionality.
 */
module.exports = class BasisCustomContentToolPlugin {
  apply(compiler) {

    /**
     * Check if required environment variable is set.
     */
    if (process.env.CUSTOM_CONTENT_TOOL !== 'true') {
      return
    }

    /**
     * `thisCompilation` is run every time.
     */
    compiler.hooks.thisCompilation.tap(pluginName, (compilation) => {
      compilation.hooks.processAssets.tapAsync(
        {
          name: pluginName,
          stage: Compilation.PROCESS_ASSETS_STAGE_OPTIMIZE,
        },
        async(_, callback) => {
          await this.updateLiquidSections()
          callback()
        },
      )
    })
  }

  /**
   * Find all Liquid files in dist/sections/ and update them.
   * @returns {Promise}
   */
  updateLiquidSections() {
    return new Promise(async(resolve, reject) => {
      try {
        const files = fileSync(Paths.dist.sections, ['liquid'])

        const queue = [...files].map((filepath) => {
          return this.updateLiquidFile(filepath)
        })

        await Promise.all(queue)
        debug('Custom content tool', 'Updated and duplicated sections', 'bgYellow')
        resolve()

      } catch (error) {
        debug('Custom content tool 1', error, 'bgRed')
        reject(error)
      }
    })
  }

  /**
   * Update section Liquid file.
   * - Add section schema attribute value.
   * - Create duplicate snippet if the section has a data-section-schema
   *   attribute with the following changes:
   *   - Remove {% schema %} tag.
   *   - Remove data-section-schema and Liquid variable.
   *   - Update introductory comment.
   * @param {String} filepath - Path to section in assets.
   * @returns {Promise}
   */
  updateLiquidFile(filepath) {
    return new Promise(async(resolve, reject) => {
      const handle = filepath
        .split(/[\\/]+/g)
        .reverse()[0]
        .replace('.liquid', '')

      try {
        const contents = await fs.readFile(filepath, 'utf-8')

        if (!contents.includes('data-section-schema')) {
          resolve()
          return
        }

        /**
         * Create snippet file.
         */
        const snippetFile = contents
          .replace(/{% schema %}.+{% endschema %}/gs, '')
          .replace(/\s{2}data-section-schema\n/g, '')
          .replace(
            /Section(?<comment>:.+)/g,
            (_, $1) => `Snippet${$1}\n  - Custom content tool section duplicate.\n  - This file is automatically generated, do not edit.`,
          )

        const snippetPath = path.resolve(Paths.dist.snippets, `cct-${handle}.liquid`)

        await fs.writeFile(snippetPath, snippetFile, 'utf-8')

        /**
         * Replace data attribute value with Liquid.
         */
        // {%- capture section_schema -%}
        //   {% if request.design_mode %}
        //     {
        //       "section": {
        //         "id": {{ section.id | json }},
        //         "type": "${handle}",
        //         "settings": {{ section.settings | json }},
        //         "blocks": [
        //           {% for block in section.blocks %}
        //             {
        //               "type": {{ block.type | json }},
        //               "settings": {{ block.settings | json }}
        //             }{% unless forloop.last %},{% endunless %}
        //           {% endfor %}
        //         ]
        //       }
        //     }
        //   {% endif %}
        // {%- endcapture -%}
        // {{ section_schema | strip | escape }}

        const liquid = `{%- capture section_schema -%}{% if request.design_mode %}{"section": { "id": {{ section.id | json }}, "type": "${handle}", "settings": {{ section.settings | json }}, "blocks": [{% for block in section.blocks %}{ "type": {{ block.type | json }}, "settings": {{ block.settings | json }} }{% unless forloop.last %},{% endunless %}{% endfor %}]}}{% endif %}{%- endcapture -%}{{ section_schema | strip | escape }}`

        const sectionFile = contents
          .replace(
            /data-section-schema(?:=".*")?/g,
            `data-section-schema='${liquid}'`,
          )

        await fs.writeFile(filepath, sectionFile, 'utf-8')
        resolve()

      } catch (error) {
        Tny.message(Tny.colour('red', `❌ Failed to update ${handle} section for custom content tool`))
        debug('Custom content tool 1', error, 'bgRed')
        reject(error)
      }
    })
  }
}
